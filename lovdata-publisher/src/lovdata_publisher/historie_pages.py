"""Render historie/{slug}.md amendment timelines to standalone HTML pages.

Reads `historie/*.md` (generated by historie.py from amendments.db) and
writes per-law amendment timeline pages to `_site/historie/*.html`, linked
from per-law pages via the historikk section.

The historie markdown lists every amendment to every paragraph of every law
since 2001, with the amendment instruction (e.g. "§ 3-1 første ledd skal lyde:")
and the new text as a blockquote. This makes it the most actionable view for
someone asking "what changed in this law lately?" without leaving the site.
"""
