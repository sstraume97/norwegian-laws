def _parse_frontmatter(filepath: Path) -> tuple[dict, str]:
    text = filepath.read_text(encoding="utf-8")
    if not text.startswith("---"):
        return {}, text
    try:
        end = text.index("\n---", 4)
    except ValueError:
        return {}, text
    fm_block = text[4:end]
    body = text[end + 4:].lstrip("\n")
    meta = {}
    for line in fm_block.splitlines():
        m = re.match(r'(\S+?):\s*"(.+?)"\s*$', line)
        if m:
            meta[m.group(1)] = m.group(2)
    return meta, body


def _build_toc(body_html: str) -> str:
    """Extract H2 headings (paragraph numbers) and build a TOC.

    The H2 may contain a trailing per-paragraph history link (added by
    _add_heading_anchors); strip that before generating the anchor.
    """
    headings = re.findall(r'<h2\s+id="([^"]+)">(.+?)</h2>', body_html, re.DOTALL)
    if not headings:
        return ""
    lines = ['<div class="toc"><strong>Paragrafer med endringer:</strong>', '<ul>']
    for anchor, raw_text in headings:
        # raw_text might be 'X-Y <a ...>...</a>'; keep only text before <a>
        clean = re.sub(r'\s*<a\s.*$', '', raw_text, flags=re.DOTALL).strip()
        if not clean:
            continue
        lines.append(f'  <li><a href="#{anchor}">{clean}</a></li>')
    lines.append('</ul></div>')
    return "\n".join(lines)


def _add_heading_anchors(body_html: str, law_stem: str | None = None) -> str:
    """Add id= attributes to H2 headings so TOC links resolve. When law_stem
    is provided, also append a small '⧉ paragraf-historikk' link next to
    each paragraph H2, pointing at the corresponding per-paragraph history
    page generated by paragraph_history.py."""
    def repl(match):
        text = match.group(1)
        anchor = re.sub(r'[^a-z0-9]+', '-', text.lower()).strip('-')
        link = ""
        if law_stem:
            # Normalize 'X-Y[letter]' from the heading text
            m = re.match(r'§\s*(\d+[-–]\d+[a-z]?)', text.strip())
            if m:
                para_norm = m.group(1).replace("–", "-")
                para_slug = f"para-{para_norm}"
                link = (
                    f' <a class="para-history-link" '
                    f'href="../historikk/{law_stem}/{para_slug}.html" '
                    f'title="Detaljert endringshistorikk for § {para_norm}" '
                    f'style="font-size:0.65em;font-weight:normal;color:#2780e3;'
                    f'text-decoration:none;padding-left:0.5em;">⧉ paragraf</a>'
                )
        return f'<h2 id="{anchor}">{text}{link}</h2>'
    return re.sub(r'<h2[^>]*>(§\s*[^<]+?)</h2>', repl, body_html)


def _render_body(body_md: str) -> str:
    if md is None:
        # Fallback: minimal escaping
        import html
        return f"<pre>{html.escape(body_md)}</pre>"
    return md.markdown(body_md, extensions=["fenced_code", "tables", "nl2br"])


def _feed_stem_for(refid: str) -> str:
    if refid.startswith("forskrift/"):
        return "forskrift-" + refid.split("/", 1)[1]
    return "lov-" + refid.split("/", 1)[1]


def _law_url_for(refid: str) -> str:
    if refid.startswith("forskrift/"):
        return f"../forskrifter/forskrift-{refid.split('/', 1)[1]}.html"
    return f"../lover/lov-{refid.split('/', 1)[1]}.html"


def _law_path_for(refid: str) -> str:
    if refid.startswith("forskrift/"):
        return f"forskrifter/forskrift-{refid.split('/', 1)[1]}.md"
    return f"lover/lov-{refid.split('/', 1)[1]}.md"


def generate_historie_pages(
    historie_dir: str = "historie",
    site_dir: str = "_site",
) -> dict[str, str]:
    """Render every historie/*.md file to _site/historie/*.html.

    Returns a manifest of refid → output relative path for cross-linking.
    """
    src = Path(historie_dir)
    out = Path(site_dir) / "historie"
    if not src.is_dir():
        print(f"  {src} not found, skipping historie HTML generation")
        return {}

    out.mkdir(parents=True, exist_ok=True)
    manifest = {}
    count = 0

    for md_file in sorted(src.glob("*.md")):
        meta, body_md = _parse_frontmatter(md_file)
        refid = meta.get("refid", "")
        title = meta.get("tittel", md_file.stem)
        short = re.sub(r"\s*–\s*.*$", "", title).strip() or title
        if not refid:
            continue

        body_html = _render_body(body_md)
        # law_stem is needed to build the per-paragraph history page URL
        # (e.g. 'lov-1998-07-17-56' so paragraph H2s can link to
        # /historikk/lov-1998-07-17-56/para-1-2a.html)
        law_stem = refid.replace("/", "-")
        body_html = _add_heading_anchors(body_html, law_stem=law_stem)
        toc_html = _build_toc(body_html)
        body_html = toc_html + body_html

        page = PAGE_TEMPLATE.format(
            title=title,
            short=short,
            feed_stem=_feed_stem_for(refid),
            law_url=_law_url_for(refid),
            law_path=_law_path_for(refid),
            body=body_html,
        )
        out_file = out / f"{md_file.stem}.html"
        out_file.write_text(page, encoding="utf-8")
        manifest[refid] = f"historie/{md_file.stem}.html"
        count += 1

    print(f"  Generated {count} historie HTML pages at {out}")
    return manifest


if __name__ == "__main__":
    import sys
    h_dir = sys.argv[1] if len(sys.argv) > 1 else "historie"
    s_dir = sys.argv[2] if len(sys.argv) > 2 else "_site"
    generate_historie_pages(h_dir, s_dir)
